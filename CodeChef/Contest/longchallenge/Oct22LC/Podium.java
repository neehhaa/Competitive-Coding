package Contest.longchallenge.OctLC;

import java.util.Scanner;

public class Podium {
    public static void main(String[] args) {
        Scanner input = new Scanner(System.in);
        int testcase = input.nextInt();
        while (testcase-- > 0) {
            int a = input.nextInt();
            int b = input.nextInt();

            System.out.println(a + b);
        }
    }
}
